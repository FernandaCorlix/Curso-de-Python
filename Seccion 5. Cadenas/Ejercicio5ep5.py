'''
Hacer un programa donde se cuente cada una de las vocales en una cadena,
mostrar el conteo de las apariciones de cada vocal
'''

cadena = input("Ingresa una cadena:\n ")

print(f"\n Vocal a: {cadena.count('a')}")
print(f"\n Vocal e: {cadena.count('e')}")
print(f"\n Vocal i: {cadena.count('i')}")
print(f"\n Vocal o: {cadena.count('o')}")
print(f"\n Vocal u: {cadena.count('u')}")
